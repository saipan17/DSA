class ThirdLargeEle
{
    int thirdLargest(int a[], int n)
    {
	    for(int i = 0; i<n; i++){
	        
	         for(int j = i+1; j<n; j++){
	             
	             if(a[i]>a[j]){
	                 
	                 int temp = a[i];
	                 a[i] = a[j];
	                 a[j] = temp;
	             }
	         }
	    }
	    return a[n-3];
    }
}
